package ch1_variables;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int x= 10;
        int y = 5;

        int z = 10+5;
        int k = x+y+1;
        System.out.println(z);
        System.out.println(k);

        System.out.println(x-2);
        System.out.println(x*2);
        System.out.println(x/y);
        System.out.println(10 % 3);



    }

}
